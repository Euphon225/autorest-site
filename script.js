function updateContent() {
  // Update inner HTML tags
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    element.innerHTML = i18next.t(key);
  });
  
  // Update attributes (like placeholder)
  document.querySelectorAll('[data-i18n-attr]').forEach(element => {
    const attrRules = element.getAttribute('data-i18n-attr').split(';');
    attrRules.forEach(rule => {
      const [attr, key] = rule.split('|');
      element.setAttribute(attr, i18next.t(key));
    });
  });

  // Hot swap logic for the hero image dynamically
  const heroImage = document.getElementById('hero-image');
  const appStoreBadge = document.getElementById('app-store-badge');
  if (heroImage) {
    // Determine the current resolved language (en, fr, es, de)
    let lang = 'en';
    if(i18next.resolvedLanguage) {
        lang = i18next.resolvedLanguage.split('-')[0]; // fallback strip locale
    }
    // Handle fallback if language isn't directly supported in assets
    const supportedImages = ['en', 'fr', 'es', 'de'];
    if(!supportedImages.includes(lang)) { lang = 'en'; }

    // Update src
    heroImage.src = `autorest-${lang}.png`;

    if (appStoreBadge) {
        const badgeUrls = {
            'en': 'https://tools.applemediaservices.com/api/badges/download-on-the-app-store/black/en-us?size=250x83',
            'fr': 'https://tools.applemediaservices.com/api/badges/download-on-the-app-store/black/fr-fr?size=250x83',
            'es': 'https://tools.applemediaservices.com/api/badges/download-on-the-app-store/black/es-es?size=250x83',
            'de': 'https://tools.applemediaservices.com/api/badges/download-on-the-app-store/black/de-de?size=250x83'
        };
        appStoreBadge.src = badgeUrls[lang] || badgeUrls['en'];
    }
  }
}

document.addEventListener('DOMContentLoaded', () => {
    
  // Initialize i18next
  // Note: assumes browser globals i18next and i18nextBrowserLanguageDetector are loaded
  if(window.i18next && window.i18nextBrowserLanguageDetector) {
    i18next
      .use(i18nextBrowserLanguageDetector)
      .init({
        fallbackLng: 'en',
        supportedLngs: ['en', 'fr', 'es', 'de'],
        resources: translations,
        detection: {
            order: ['querystring', 'cookie', 'localStorage', 'navigator', 'htmlTag'],
            caches: ['localStorage', 'cookie'],
        }
      }).then(() => {
        
        // Initial translation update after document loads
        updateContent();
        
        // Setup language select dropdown listener
        const langSelector = document.getElementById('lang-switcher');
        if (langSelector) {
            // Set default select option based on detected language
            const activeLang = i18next.resolvedLanguage ? i18next.resolvedLanguage.split('-')[0] : 'en';
            if(['en', 'fr', 'es', 'de'].includes(activeLang)) {
                langSelector.value = activeLang;
            }
            
            // Listen for changes
            langSelector.addEventListener('change', (e) => {
                const newLang = e.target.value;
                i18next.changeLanguage(newLang).then(() => {
                    updateContent();
                });
            });
        }
      });
  }

  // Smooth scroll for anchor links
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      e.preventDefault();
      
      const targetId = this.getAttribute('href');
      if (targetId === '#') return;
      
      const targetElement = document.querySelector(targetId);
      if (targetElement) {
        window.scrollTo({
          top: targetElement.offsetTop - 100, // Account for fixed navbar
          behavior: 'smooth'
        });
      }
    });
  });

  // Intersection Observer for fade-up animations
  const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.15
  };

  const observer = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target); // Stop observing once visible
      }
    });
  }, observerOptions);

  // Select all elements with the fade-up class and observe them
  const fadeElements = document.querySelectorAll('.fade-up');
  fadeElements.forEach(el => observer.observe(el));

  const pageclipForm = document.querySelector('.pageclip-form');
  if (pageclipForm) {
    pageclipForm.addEventListener('submit', function(e) {
      console.log('Données envoyées :', new FormData(this).get('email'));
    });
  }
});
